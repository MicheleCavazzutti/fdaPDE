template <class T_Type> int
Param_Map::Get_Arg_Typed
(   std::string par,
    T_Type& arg )
{
    Param_Map::const_iterator iter = find(par);

    if (iter == end())
        return 0;

    if (Converter<T_Type>(iter->second, arg) != 0)
        return 0;

    return 1;
}
